package com.example.myapplication.user_interface.dashboard.model;

public class Drilldown {

    private String mLeadBy;
    private String mOrganisationName;
    private String mLeadEstimate;
    private String mProduct;
    private String mLeadNo;
    private String mResponsibleUser;
    private String mLocation;



    public String getLeadBy() {
        return mLeadBy;
    }

    public void setLeadBy(String leadBy) {
        mLeadBy = leadBy;
    }

    public String getOrganisationName() {
        return mOrganisationName;
    }

    public void setOrganisationName(String organisationName) {
        mOrganisationName = organisationName;
    }

    public String getLeadEstimate() {
        return mLeadEstimate;
    }

    public void setLeadEstimate(String leadEstimate) {
        mLeadEstimate = leadEstimate;
    }

    public String getProduct() {
        return mProduct;
    }

    public void setProduct(String product) {
        mProduct = product;
    }

    public String getLeadNo() {
        return mLeadNo;
    }

    public void setLeadNo(String leadNo) {
        mLeadNo = leadNo;
    }

    public String getResponsibleUser() {
        return mResponsibleUser;
    }

    public void setResponsibleUser(String responsibleUser) {
        mResponsibleUser = responsibleUser;
    }

    public String getLocation() {
        return mLocation;
    }

    public void setLocation(String location) {
        mLocation = location;
    }
}
